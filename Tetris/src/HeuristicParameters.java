

public class HeuristicParameters {
	
	protected double a;
	protected double b;
	protected double c;
	protected double d;
	
	public HeuristicParameters() {
		this.a = 0;
		this.b = 0;
		this.c = 0;
		this.d = 0;
	}
	
	public HeuristicParameters(double a, double b, double c, double d) {
		this.a = a;
		this.b = b;
		this.c = c;
		this.d = d;
	}
	
}
